package aem.example.java.behavioral.mediator;
/*
 * Mediator pattern defines an object that encapsulates how a set of objects interact. Mediator promotes loose coupling
 * by keeping objects from referring to each other explicitly, and it lets us vary their interaction independently.
 *
 * The mediator object’s architecture may become complex if you put too much logic inside it.
 * An inappropriate use of the mediator pattern may end up with a “God Class” anti-pattern.
 *
 *
 * https://howtodoinjava.com/design-patterns/behavioral/mediator-pattern/
 */